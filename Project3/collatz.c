#include <stdio.h>
extern int collatz(int n);

void main (int argc, char *argv[]){                    //takes number as parameter
  int i;
  for (i = 1; i <= atoi(argv[1]); i++){
    printf("n = %d     : %d\n", i, collatz(i));        //prints stopping time of all numbers less than the number
  }
}

